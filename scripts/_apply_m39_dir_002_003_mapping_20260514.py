    lines = [
        f"# WA-{spec['segment']}-group-mapping-applied-v1-20260514",
        "",
        f"**Directive:** {directive_id}",
        f"**Segment:** {spec['segment']}",
        f"**Source directive:** [{spec['directive_file']}]({os.path.relpath(spec['directive_file'])})",
        f"**Applied:** {now_utc}",
        f"**Mode:** {outcome['mode']}",
        "",
        "## Operations applied",
        "",
        f"- Op 1 — `verse_context_group` `context_description` UPDATE: **{outcome.get('vcg_refines', 0)}** rows",
        f"- Op 2 — `verse_context` `group_id` UPDATE: **{outcome.get('vc_assigns', 0)}** rows",
        f"- No NEW VCGs → no `vcg_term` INSERTs",
        "",
        "## Post-state",
        "",
        f"- Remaining P-status verses in {spec['segment']} (is_relevant=1, group_id IS NULL): **{outcome.get('remaining_p_in_segment', '?')}** (expected 0)",
        "",
        "## Schema deviations from directive text",
        "",
        "Intent applied per actual schema. Surface for AI's future reference:",
        "",
        "| Directive said | Actual column |",
        "|---|---|",
        "| `verse_context_group.subgroup_code` | `group_code` |",
        "| `verse_context_group.core_description` | `context_description` |",
    ]
    with open(spec["report_file"], "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")
    print(f"\n  Report → {spec['report_file']}")


