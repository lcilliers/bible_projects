def apply_directive(conn, directive_id, spec, now_utc, live):
    """Apply one directive as a single transaction. Returns dict of outcomes."""
    segment = spec["segment"]
    vcg_refines = spec["vcg_refines"]
    vc_assigns = spec["vc_assigns"]

    print(f"\n{'=' * 60}")
    print(f"  {directive_id} — segment {segment}")
    print(f"{'=' * 60}")
    print(f"  Op 1: {len(vcg_refines)} verse_context_group context_description UPDATEs")
    print(f"  Op 2: {len(vc_assigns)} verse_context group_id UPDATEs")

    # Pre-flight (every reference resolves)
    for vcg_id, _desc in vcg_refines:
        r = conn.execute("SELECT id FROM verse_context_group WHERE id=? AND COALESCE(delete_flagged,0)=0",
                         (vcg_id,)).fetchone()
        if not r:
            raise SystemExit(f"❌ [{directive_id}] verse_context_group id={vcg_id} not found / deleted")

    for vr_id, mti_id, target_gid in vc_assigns:
        vc = conn.execute("""SELECT id, group_id, is_relevant
                             FROM verse_context
                             WHERE verse_record_id=? AND mti_term_id=?
                               AND COALESCE(delete_flagged,0)=0""",
                          (vr_id, mti_id)).fetchone()
        if not vc:
            raise SystemExit(f"❌ [{directive_id}] vc row (vr={vr_id}, mti={mti_id}) not found")
        tg = conn.execute("SELECT id FROM verse_context_group WHERE id=? AND COALESCE(delete_flagged,0)=0",
                          (target_gid,)).fetchone()
        if not tg:
            raise SystemExit(f"❌ [{directive_id}] target group_id={target_gid} not found")
    print(f"  ✓ Pre-flight passed: all refs resolve")

    if not live:
        return {"mode": "dry-run"}

    # Live apply — single transaction per directive
    try:
        for vcg_id, new_desc in vcg_refines:
            conn.execute(
                "UPDATE verse_context_group SET context_description=? WHERE id=?",
                (new_desc, vcg_id)
            )
        for vr_id, mti_id, target_gid in vc_assigns:
            cur = conn.execute("""UPDATE verse_context
                                  SET group_id=?
                                  WHERE verse_record_id=? AND mti_term_id=?
                                    AND COALESCE(delete_flagged,0)=0""",
                               (target_gid, vr_id, mti_id))
            if cur.rowcount != 1:
                raise SystemExit(
                    f"❌ [{directive_id}] UPDATE verse_context (vr={vr_id}, mti={mti_id}) "
                    f"affected {cur.rowcount} rows (expected 1)"
                )
        conn.commit()
    except Exception:
        conn.rollback()
        raise

    # COMPLETION CONFIRMATION
    print(f"\n  COMPLETION CONFIRMATION for {directive_id}:")

    # Q1 — Sample VCG description spot-check
    for vcg_id, expected_desc in vcg_refines[:2]:
        r = conn.execute("SELECT id, group_code, context_description FROM verse_context_group WHERE id=?",
                         (vcg_id,)).fetchone()
        match = r["context_description"] == expected_desc
        marker = "✓" if match else "❌"
        print(f"    {marker} vcg {r['id']} ({r['group_code']}): context_description {'matches' if match else 'MISMATCH'}")
        if not match:
            raise SystemExit(f"❌ [{directive_id}] VCG {vcg_id} desc mismatch after write")

    # Q2 — verse_context group_id assignments
    assigned = 0
    for vr_id, mti_id, target_gid in vc_assigns:
        r = conn.execute("""SELECT group_id FROM verse_context
                            WHERE verse_record_id=? AND mti_term_id=?""",
                         (vr_id, mti_id)).fetchone()
        if r and r["group_id"] == target_gid:
            assigned += 1
    print(f"    Q2: {assigned}/{len(vc_assigns)} verse_context rows have target group_id")
    if assigned != len(vc_assigns):
        raise SystemExit(f"❌ [{directive_id}] assignment mismatch")

    # Q3 — Remaining P-status in this sub-group
    remaining_p = conn.execute("""
        SELECT COUNT(*) c
        FROM verse_context vc
        JOIN mti_terms mt ON mt.id = vc.mti_term_id
        JOIN mti_term_subgroup mts ON mts.mti_term_id = mt.id AND COALESCE(mts.delete_flagged,0)=0
        JOIN cluster_subgroup cs ON cs.id = mts.cluster_subgroup_id
        WHERE cs.cluster_code='M39' AND cs.subgroup_code=?
          AND vc.is_relevant=1 AND vc.group_id IS NULL
    """, (segment,)).fetchone()["c"]
    print(f"    Q3: Remaining P-status (is_relevant=1, group_id NULL) in {segment}: {remaining_p}")

    return {
        "mode": "live",
        "vcg_refines": len(vcg_refines),
        "vc_assigns": len(vc_assigns),
        "remaining_p_in_segment": remaining_p,
    }


